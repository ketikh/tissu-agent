def get_payment_agent() -> AgentDefinition:
    return AgentDefinition(
        name="Payment Agent",
        agent_type="payment",
        system_prompt=PAYMENT_PROMPT,
        tools=[
            Tool(
                name="create_order",
                description="შეკვეთის შექმნა — მისამართი და ტელეფონი ორივე რომ გექნება.",
                parameters={
                    "type": "object",
                    "properties": {
                        "customer_name": {"type": "string"},
                        "customer_phone": {"type": "string"},
                        "customer_address": {"type": "string"},
                        "items": {"type": "string"},
                        "total": {"type": "number"},
                        "payment_method": {"type": "string"},
                    },
                    "required": ["customer_name", "customer_phone", "customer_address", "items", "total"],
                },
                handler=create_order,
            ),
        ],
    )
